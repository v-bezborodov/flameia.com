                    <li>select the icon in the poped-up Info Pane (by simply clicking on picture)</li>
                    <li>issue Copy command (Command+C)</li>
                    <li>issue GetInfo command for the desired document or folder</li>
                    <li>select an old icon in the poped-up Info Pane</li>
                    <li>issue Paste command (Command+V)</li>
                </ul>
                The icon will be replaced for the previously selected one.<br><br>
